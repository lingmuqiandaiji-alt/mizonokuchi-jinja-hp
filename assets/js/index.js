(function () {
  'use strict';

  /* ------------------------------------------------------------
     お知らせ：0件時の表示制御
     - .news-list 内に .news-item が1件もない場合、
       「現在お知らせはありません」(#news-empty) を表示する。
     - JSが無効の場合はHTMLに書かれたままの状態で表示される
       （サンプル記事はHTML側で管理する）。
     ------------------------------------------------------------ */
  var newsList = document.getElementById('news-list');
  var newsEmpty = document.getElementById('news-empty');

  if (newsList && newsEmpty) {
    var items = newsList.querySelectorAll('.news-item');
    if (items.length === 0) {
      newsList.hidden = true;
      newsEmpty.hidden = false;
    }
  }

  /* ------------------------------------------------------------
     将来拡張メモ（第2回以降）
     - 行事データの一元管理（JSONによる年次更新）
     - お知らせ一覧ページとの連携
     - 境内マップの地点選択型案内
     いずれも「JSなしでも基本情報が読める」原則を維持すること。
     ------------------------------------------------------------ */
})();
